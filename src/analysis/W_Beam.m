        function Ag = AgSolver (perfil) %Calcula a rea bruta da seo transversal de um perfil laminado
           
            Ag = 2*perfil.bf*perfil.tf + perfil.h*perfil.tw;  
            
          
        
           
        end
        
        function Ix = IxSolver (perfil) %Calcula o momento de inercia em relao ao eixo X de um perfil laminado
            Ix = (2*perfil.bf*perfil.tf.^3)/12 + (perfil.tw*perfil.h.^3)/12 + 2*(perfil.bf*perfil.tf * ((perfil.d-perfil.tf)/2).^2);    
        end
        
        function Iy = IySolver (perfil) %Calcula o momento de inercia em relao ao eixo Y de um perfil laminado
            Iy = (2*perfil.tf*perfil.bf.^3)/12 + (perfil.h * perfil.tw.^3)/12;
        end
        
        function Iyef = IyefSolver (perfil) %Calcula o momento de inercia efetivo em relao ao eixo Y de um perfil laminado (Caso tenha ocorrido reduo do bef)
            Iyef = (2*perfil.tf*perfil.bf.^3)/12 + (perfil.hef * perfil.tw.^3)/12;
        end
        
        function r0 = r0Solver (perfil) %Calcula o raio de girao
            r0 = sqrt(perfil.rx^2+perfil.ry^2);
        end
        
        function rx = rxSolver (perfil)
            rx = sqrt(perfil.Ix/perfil.Ag);
        end
        
        function ry = rySolver (perfil) 
            ry = sqrt(perfil.Iy/perfil.Ag);
        end
        
        function Wx = WxSolver (perfil) %Calcula o Wx em [mm^3] - Modulo de resistncia Elstico (eixo forte)
           Wx = perfil.Ix/(perfil.d/2); 
        end
        
        
        function Wy = WySolver (perfil) %Calcula o Wy em [mm^3] - Modulo de resistncia Elstico (eixo fraco)
            Wy = (perfil.Iy/(perfil.bf/2));
        end
        
        function Wyef = WyefSolver (perfil) %Calcula o Wyef em [mm^3] - Modulo de resistncia Elstico efetivo (ocorreu reduo do bef)
            Wyef = (perfil.Iyef/(perfil.bf/2));
        end
        
        function cw = cwSolver (perfil) %Calcula o cw em [MPa] - Constante de Empenamento da seo transveral
            cw = (perfil.Iy * (perfil.d-perfil.tf).^2)/4; 
        end
        
        function J = JSolver (perfil) %Calcula o J em [mm^4] - Constante de Toro
            J = 1/3*(2*perfil.bf * perfil.tf.^3 + (perfil.h+2*perfil.tf) * perfil.tw.^3); %CONFERIR ESSE H IMPORTANTE
        end
        


         
         
        
        function lne = neSolver (perfil,Gparam,Distparam)
            ne = (1:3);
