        function q = qSolver (perfil,Gparam) %Calcula o fator de reduao para a alma e para a mesa
            lambidaMesa = (perfil.bf/2)/(perfil.tf);   
            lambidaLimite1 = 0.56*sqrt(Gparam.Ea/Gparam.fy); 
            lambidaLimite2 = 1.03*sqrt(Gparam.Ea/Gparam.fy);  
            if lambidaMesa < lambidaLimite1     
                qMesa = 1;
            elseif lambidaMesa < lambidaLimite2    
                qMesa = 1.415-0.74*lambidaMesa*sqrt(Gparam.fy/Gparam.Ea);
            else
                qMesa = 0.69*Gparam.Ea/(Gparam.fy*lambidaMesa.^2);
            end
            lambidaAlma = (perfil.h)/(perfil.tw);
            lambidaLimite1 = 1.49*sqrt(Gparam.Ea/Gparam.fy);
            if lambidaAlma < lambidaLimite1
                qAlma = 1;
            else
                bef = 1.92 * perfil.tw * sqrt(Gparam.Ea / Gparam.fy)*(1-0.34/lambidaAlma*sqrt(Gparam.Ea / Gparam.fy));
                if bef > perfil.h
                    qAlma = 1;
                else
                    Aef = perfil.Ag - (perfil.h-bef)*perfil.tw;
                    qAlma = Aef/perfil.Ag;
                end
            end
            q = qAlma * qMesa;
        end
        
