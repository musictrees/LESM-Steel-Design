        function [enrijec1, enrijec2] = EnrijecedorSolver (perfil,Gparam,Distparam,Strparam)
            enrijec1 = 0;
            enrijec2 = 0;
            limite = (260/(perfil.h/perfil.tw)).^2;
            if limite > 3
                limite = 3;
            end
            vpl = 0.6*perfil.d*perfil.tw*Gparam.fy;
            lambidaAlma = perfil.h/perfil.tw;
            lambidaLimite1 = 1.10 * sqrt(5 * Gparam.Ea/Gparam.fy);
            lambidaLimite2 = 1.37 * sqrt(5 * Gparam.Ea/Gparam.fy);
            if lambidaAlma < lambidaLimite1
                vrdpure = vpl/1.1; %Representa o Vrd sem enrijecedor
            elseif lambidaAlma < lambidaLimite2
                vrdpure = lambidaLimite1/lambidaAlma * vpl/1.1;
            else
                vrdpure = 1.24 * (lambidaLimite1/lambidaAlma).^2 * vpl/1.1;
            end
            syms c1
            c1 = double(solve(Strparam.cort2 == Strparam.cort1 +Distparam.lb*c1,c1));
            if vrdpure < Strparam.cort1
                syms x1
                x1 = double(solve(abs(vrdpure) == Strparam.cort1 +c1*x1,x1));
                i = 0;
                while 1
                    i = i+1;
                    a = x1/(i+1);
                    if a/perfil.h > limite
                        kv = 5;
                    else
                        kv = 5 + 5/(a/perfil.h).^2;
                    end
                    lambidaLimite1 = 1.10 * sqrt(kv * Gparam.Ea/Gparam.fy);
                    lambidaLimite2 = 1.37 * sqrt(kv * Gparam.Ea/Gparam.fy);
                    if lambidaAlma < lambidaLimite1
                         vrd = vpl/1.1; 
                    elseif lambidaAlma < lambidaLimite2
                        vrd = lambidaLimite1/lambidaAlma * vpl/1.1;
                    else
                        vrd = 1.24 * (lambidaLimite1/lambidaAlma).^2 * vpl/1.1;
                    end
                    if vrd > Strparam.cort1
                        enrijec1 = i;
                        break
                    end
                end
            end
            if vrdpure < abs(Strparam.cort2)
                syms x2
                if Strparam.cort2 < 0
                    x2 = double(solve(-1*vrdpure == Strparam.cort1 +c1*x2,x2));
                else
                    x2 = double(solve(vrdpure == Strparam.cort1 +c1*x2,x2));
                end
                i = 0;
                while 1
                    i = i+1;
                    a = (Distparam.lb-x2)/(i+1);
                    if a/perfil.h > limite
                        kv = 5;
                    else
                        kv = 5 + 5/(a/perfil.h).^2;
                    end
                    lambidaLimite1 = 1.10 * sqrt(kv * Gparam.Ea/Gparam.fy);
                    lambidaLimite2 = 1.37 * sqrt(kv * Gparam.Ea/Gparam.fy);
                    if lambidaAlma < lambidaLimite1
                        vrd = vpl/1.1; 
                    elseif lambidaAlma < lambidaLimite2
                        vrd = lambidaLimite1/lambidaAlma * vpl/1.1;
                    else
                        vrd = 1.24 * (lambidaLimite1/lambidaAlma).^2 * vpl/1.1;
                    end
                    if vrd > Strparam.cort1
                        enrijec2 = i;
                        break
                    end
                end
            end
        end
    end
